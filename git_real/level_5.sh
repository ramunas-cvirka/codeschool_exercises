# PUSH BRANCH
git push origin hamsters

# GET REMOTE BRANCH
git fetch

# REMOTE BRANCHES
git branch -r

# DELETE ON REMOTE
git push origin :weasel

# BRANCH STATUS
git remote show origin

# CLEAN BRANCHES
git remote prune origin

# LIST TAGS
git tag

# CREATE TAG
git tag -a v1.3.2 -m 'version 1.3.2'

# SEND TAGS
git push --tags

# RETRIEVE TAG
git checkout v1.3.1
